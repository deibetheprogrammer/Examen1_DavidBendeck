    public static int getEstudiante(String username, ArrayList<Estudiante> users) {
        for (int i = 0; i < users.size(); i++) {
            Alumno a = users.get(i);
            if (a.getUsername().equals(username)) {
                return i;
            }
        }
        return -1;
    }
